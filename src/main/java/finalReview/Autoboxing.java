package finalReview;

public class Autoboxing {
    public static void main(String[] args) {
        //Autoboxing of char
        Character gfg = 'a';

        // unboxing of Character
        char ch = gfg;
        System.out.println("Value of ch: " + ch);
        System.out.println("Value of gfg: " + gfg);
    }
}
